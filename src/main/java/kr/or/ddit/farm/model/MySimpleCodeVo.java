public class MySimpleCodeVo {

	private String my_simple_code;
	private String owner;
	private String item_code;
	private String bsn_code;
	private String alias;
	private String area;
	private String use;

	public String getMy_simple_code() {
		return my_simple_code;
	}

	public void setMy_simple_code(String my_simple_code) {
		this.my_simple_code = my_simple_code;
	}

	public String getOwner() {
		return owner;
	}

	public void setOwner(String owner) {
		this.owner = owner;
	}

	public String getItem_code() {
		return item_code;
	}

	public void setItem_code(String item_code) {
		this.item_code = item_code;
	}

	public String getBsn_code() {
		return bsn_code;
	}

	public void setBsn_code(String bsn_code) {
		this.bsn_code = bsn_code;
	}

	public String getAlias() {
		return alias;
	}

	public void setAlias(String alias) {
		this.alias = alias;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}

	public String getUse() {
		return use;
	}

	public void setUse(String use) {
		this.use = use;
	}

}
